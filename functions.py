def int2bin(n):
    '''
    自然数 n を2進数（バイナリコード）に変換します．
    '''
    if n>0:
        bits = []
        while n>0:
            n,remainder = divmod(n, 2)
            bits.insert(0, remainder)
        return bits
    else:
         return [0]
 
def bin2gray(bits):
    '''
    バイナリコードをグレイコードに変換します．
    '''
    return bits[:1] + [i ^ ishift for i, ishift in zip(bits[:-1], bits[1:])]

